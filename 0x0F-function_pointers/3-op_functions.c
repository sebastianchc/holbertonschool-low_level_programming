#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - Add.
 * @a: Num1.
 * @b: Num2.
 *
 * Return: Add.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Sub.
 * @a: Num1.
 * @b: Num2.
 *
 * Return: Sub..
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Mul.
 * @a: Num1.
 * @b: Num2.
 *
 * Return: Mul.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Div.
 * @a: Num1.
 * @b: Num2.
 *
 * Return: Div.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Mod.
 * @a: Num1.
 * @b: Num2.
 *
 * Return: Mod.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
